public class Comments {
    public static void main(String[] args) {

        // This is a single-line comment
        System.out.println("This line uses a single-line comment.");

        /*
         This is a multi-line comment
         It can span across multiple lines
        */
        System.out.println("This line uses a multi-line comment.");

        /**
         * This is a documentation comment
         * It can be used to describe classes or methods
         */
        System.out.println("This line uses a documentation comment.");
    }
}
